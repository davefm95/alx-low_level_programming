/**
 *add - adds two numbers
 *@a: first number
 *@b: second number
 *Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 *sub - subs two numbers
 *@a: first number
 *@b: second number
 *Return: differnce
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 *mul - times two numbers
 *@a: first number
 *@b: second number
 *Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 *div - divides two numbers
 *@a: first number
 *@b: second number
 *Return: the reult of the div
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 *mod - gets remainder two numbers
 *@a: first number
 *@b: second number
 *Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
